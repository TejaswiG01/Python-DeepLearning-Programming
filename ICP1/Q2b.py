print("Performing Arithmetic Operations:")
no1String = input('Enter the first integer:')
no2String = input('Enter the second integer:')

no1 = int(no1String)
no2 = int(no2String)

print(no1,'+',no2,'=',no1+no2)
print(no1,'-',no2,'=',no1-no2)
print(no1,'*',no2,'=',no1*no2)
print(no1,'/',no2,'=',no1/no2)
print(no1,'%',no2,'=',no1%no2)
print(no1,'**',no2,'=',no1**no2)
